; Heart Beep Logic
;================================================================================
BeepLogic:
	LDA.l HeartBeep : BEQ +
	    STA.w HeartBeepTimer
	    LDA.b #$2B : STA.w SFX2
RTL
	+
    LDA.b #$FF : STA.w HeartBeepTimer
RTL
