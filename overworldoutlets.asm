FindOutletID:
	REP #$10
	TAX
	CPX.w #$1000
	BCS .credits

	LDA.l RoomToOutlet,X

.check_exit
	AND.w #$00FF
	CMP.w #$00FF
	BNE .good

	; temporary measure (or permanent?!)
	BRK #$00

.good
	ASL
	TAX
	RTL

.credits
	CPX.w #$101A
	BCS .fail

	LDA.l .credits_exits-$1000,X
	BRA .check_exit

.fail
	LDA.w #$FFFF
	BRA .check_exit

